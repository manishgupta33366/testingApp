package com.nga.xtendhr.fastHire.utilities;

import java.util.Map;

public class DashBoardPositionClass {
	
	private String positionTitle;
	private String positionCode;
	private String payGrade;
	private String startDate;
	private String lastUpdatedDate;
	private String userId;
	private String userFirstName;
	private String userLastName;
	private String employeeClassName;
	private String dayDiff;
	private Boolean vacant;
	private Map<String,String> statuses;
	
	public String getPositionTitle() {
		return positionTitle;
	}
	public void setPositionTitle(String positionTitle) {
		this.positionTitle = positionTitle;
	}
	public String getPositionCode() {
		return positionCode;
	}
	public void setPositionCode(String positionCode) {
		this.positionCode = positionCode;
	}
	public String getPayGrade() {
		return payGrade;
	}
	public void setPayGrade(String payGrade) {
		this.payGrade = payGrade;
	}
	public String getStartDate() {
		return startDate;
	}
	public void setStartDate(String startDate) {
		this.startDate = startDate;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getEmployeeClassName() {
		return employeeClassName;
	}
	public void setEmployeeClassName(String employeeClassName) {
		this.employeeClassName = employeeClassName;
	}
	public String getDayDiff() {
		return dayDiff;
	}
	public void setDayDiff(String dayDiff) {
		this.dayDiff = dayDiff;
	}
	public String getUserFirstName() {
		return userFirstName;
	}
	public void setUserFirstName(String userFirstName) {
		this.userFirstName = userFirstName;
	}
	public String getUserLastName() {
		return userLastName;
	}
	public void setUserLastName(String userLastName) {
		this.userLastName = userLastName;
	}
	public Boolean getVacant() {
		return vacant;
	}
	public void setVacant(Boolean vacant) {
		this.vacant = vacant;
	}
	public String getLastUpdatedDate() {
		return lastUpdatedDate;
	}
	public void setLastUpdatedDate(String lastUpdatedDate) {
		this.lastUpdatedDate = lastUpdatedDate;
	}
	public Map<String,String> getStatuses() {
		return statuses;
	}
	public void setStatuses(Map<String,String> statuses) {
		this.statuses = statuses;
	}


}
